class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        vector<int>v;
        sort(nums.begin(),nums.end());
        int l=0;
        while(l<nums.size()){
            if(nums[l]==nums[l+1]) l=l+2;
            else {
                v.push_back(nums[l]);
                l++;
             }
        }
        sort(v.begin(),v.end());
        return v;
    }
};

