class Solution {
  public:
    int countFreq(vector<int>& arr, int target) {
        // Find the first index where target appears using lower_bound
        int lb = lower_bound(arr.begin(), arr.end(), target) - arr.begin();
        
        // Find the first index after the last occurrence of target using upper_bound
        int ub = upper_bound(arr.begin(), arr.end(), target) - arr.begin();
        
        // The difference between upper and lower bounds gives the count of target
        return ub - lb;
    }
};


